//1. 두 정수 사이에 모든 정수들을 순서대로 출력하는 문제 
#include<stdio.h>
//함수 printNumber
//입력 : 두 정수 
// 출력 : 없음 
// 부수효과 없음 

void printNumber (int a,int b) {
	int count =a;
	while(count<=b)
		printf("%d\n",count++);
}


int main (void)
{
	int a =3, b=50;
	printNumber(a,b);
	printNumber(-20,100);

	printf("프로그램 종료\n");

	return 0;
}


